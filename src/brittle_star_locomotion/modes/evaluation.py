import argparse
import subprocess
import time

import jax
import jax.numpy as jnp
from flax import nnx
from pathlib import Path

from brittle_star_locomotion.config.configuration import Configuration
from brittle_star_locomotion.environment.fixedtargetenvironment import FixedTargetEnvironment
from brittle_star_locomotion.environment.randomtargetenvironment import RandomTargetEnvironment
from brittle_star_locomotion.environment.render import EnvironmentRenderer
from brittle_star_locomotion.logger.logger import Logger
from brittle_star_locomotion.neural.qnetwork import QNetwork
from brittle_star_locomotion.util.checkpoint_naming import normalize_checkpoint_base_name, resolve_agent_checkpoint_name
from brittle_star_locomotion.damage.arm_damage import ArmDamage

class Evaluator:
    def __init__(self):
        self.logger = Logger()
        self.config = Configuration().configuration
        self.environment = RandomTargetEnvironment() if self.config.environment.random_target else FixedTargetEnvironment()

    def evaluate(self, arguments: argparse.Namespace):
        """Evaluate the brittle star locomotion model.
        This method loads the trained Q-networks for each agent, runs a greedy policy rollout in the environment, and collects the trajectory data.
        The collected trajectory can be rendered to a video and saved to disk, along with the action and position trajectories in CSV format.
        
        :param arguments: Command-line arguments containing configuration and checkpoint information.
        """

        self.logger.debug("Starting evaluation process...")
        started_at = time.perf_counter()

        checkpoint_base = normalize_checkpoint_base_name(arguments.checkpoint, self.config.checkpoint_directory)
        q_networks = self._load_qnetworks_for_evaluation(
            checkpoint_base=checkpoint_base,
        )

        render_trajectory, actions_trajectory, positions_trajectory, breakpoints_trajectory, broken_arms_trajectory = self._collect_trajectory(q_networks)

        if arguments.render:
            renderer = EnvironmentRenderer(self.environment)
            renderer.render_video(render_trajectory, output_path=arguments.output_video)
            self.logger.info(f"Saved evaluation video to: {arguments.output_video}")

        self._save_actions_trajectory(arguments.output_actions_trajectory, actions_trajectory)
        self.logger.info("Saving action trajectory")

        self._save_positions_trajectory(arguments.output_positions_trajectory, positions_trajectory, self.environment.target_position)
        self.logger.info("Saving position trajectory")

        self._save_breakpoints_trajectory(arguments.output_breakpoints_trajectory, breakpoints_trajectory, broken_arms_trajectory)
        self.logger.info("Saving breakpoint trajectory")

        self._generate_charts(arguments)
        self.logger.info("Generating charts")

        elapsed = time.perf_counter() - started_at
        self.logger.info(f"Evaluation completed in {elapsed:.1f}s")
        self.logger.debug("Evaluation process completed.")

    def _load_qnetworks_for_evaluation(
        self,
        checkpoint_base: str,
    ) -> list[QNetwork]:
        """Load one Q-network checkpoint per agent for greedy policy evaluation.
        
        :param checkpoint_base: Base path name for the checkpoint to load 
        as generated by `normalize_checkpoint_base_name` in `brittle_star_locomotion.util.checkpoint_naming`

        :return: The list of `QNetwork`s initialized with the parameters in the given checkpoint
        """
        observation_size = self.environment.get_observation_size()
        checkpoint_directory = self.config.checkpoint_directory
        q_networks = []

        for agent_id in range(self.config.environment.number_of_arms):
            checkpoint_name = resolve_agent_checkpoint_name(
                checkpoint_base=checkpoint_base,
                agent_id=agent_id,
                checkpoint_directory=checkpoint_directory,
            )

            self.logger.info(f"Loading checkpoint for agent {agent_id}: {checkpoint_name}")

            q_network = QNetwork.load_checkpoint(
                model_callback=lambda agent_id=agent_id: QNetwork(
                    input_size=observation_size,
                    output_size=5,
                    rngs=nnx.Rngs(agent_id),
                    hidden_size=self.config.rl.hidden_size,
                    amount_of_hidden_layers=self.config.rl.amount_of_hidden_layers,
                ),
                name=checkpoint_name,
            )

            q_networks.append(q_network)

        return q_networks

    def _select_policy_actions(
        self,
        observations: jnp.ndarray,
        q_networks: list[QNetwork],
    ) -> jnp.ndarray:
        """Select greedy actions from the loaded per-agent Q-networks.
        
        :param observations: The observations for each agent with shape (n_environments, n_agents)
        :param q_networks: The QNetworks for each agent

        :return: The selected actions per agent with shape (n_environments, n_agents)
        """
        q_values_by_agent = []
        for agent_id, q_network in enumerate(q_networks):
            q_values_by_agent.append(
                q_network(observations[:, agent_id, :])         # shape (n_environments, n_actions)
            )

        q_values = jnp.stack(q_values_by_agent, axis=1)         # shape (n_environments, n_agents, n_actions)
        return jnp.argmax(q_values, axis=-1).astype(jnp.int32)  # shape (n_environments, n_agents)

    def _collect_trajectory(self, q_networks: list[QNetwork]):
        """Run a single greedy-policy episode and generate the renderable environment trajectory alongside extra trajectories.
        
        :param q_networks: The QNetworks for each agent

        :return: A tuple containing:
        - Renderable environment trajectory
        - Actions trajectory - shape (n_environments, n_steps, n_agents)
        - Positions trajectory - shape (n_environments, n_steps, 3)
        - Breakpoints trajectory - shape (n_environments,)
        - Broken arms trajectory - shape (n_environments,)
        """
        num_modulation_steps = self.config.gait.fixed_number_of_evaluation_modulation_steps
        num_substeps = self.config.gait.fixed_number_of_evaluation_substeps_per_modulation
        log_every = self.config.environment.render_every_x_frames

        env_state, cpg_state = self.environment.reset()
        self.logger.info(
            f"Starting policy rollout with {num_modulation_steps} modulation steps and {num_substeps} substeps per step"
        )

        done_environments = jnp.zeros((self.environment.number_of_environments,), dtype=bool)
        env_states_trajectory_list = []
        actions_trajectory_list = []
        positions_trajectory_list = []

        arm_damage = ArmDamage()

        for step_idx in range(num_modulation_steps):
            if bool(jnp.all(done_environments)):
                break

            if step_idx % max(1, log_every) == 0 or step_idx == num_modulation_steps - 1:
                self.logger.info(f"Evaluation progress: modulation step {step_idx + 1}/{num_modulation_steps}")

            observations = self.environment.get_observations()
            actions = self._select_policy_actions(observations=observations, q_networks=q_networks)
            actions = jnp.where(done_environments[:, None], 0, actions) # (n_environments, n_agents)
    
            arm_damage.break_arms(step_idx)

            env_state, cpg_state, _, terminated, truncated, trajectory = self.environment.step(
                env_state,
                cpg_state,
                actions,
                arm_damage.get_active_arms(),
                num_substeps,
            )

            terminated = jnp.asarray(terminated).reshape(-1)
            truncated = jnp.asarray(truncated).reshape(-1)
            done_environments = done_environments | terminated | truncated

            self.environment.env_state = env_state
            self.environment.cpg_state = cpg_state

            substep_env_states = trajectory[0]

            env_states_trajectory_list.append(substep_env_states)
            actions_trajectory_list.append(actions)
            positions_trajectory_list.append(self.environment.env_state.observations["disk_position"])

        self.logger.info(f"Policy rollout finished after {len(env_states_trajectory_list)} modulation steps")

        if not (env_states_trajectory_list and actions_trajectory_list):
            raise ValueError("No trajectory data collected during evaluation")

        states_trajectory = jax.tree_util.tree_map(
            lambda *xs: jnp.concatenate(xs, axis=0),
            *env_states_trajectory_list,
        )
        states_trajectory = jax.tree_util.tree_map(lambda x: jnp.swapaxes(x, 0, 1), states_trajectory)
        actions_trajectory = jnp.stack(actions_trajectory_list, axis=1)
        positions_trajectory = jnp.stack(positions_trajectory_list, axis=1)

        if self.config.damage.enabled:
            breakpoints_trajectory = arm_damage._break_points # shape (n_envs,)

            # https://chatgpt.com/share/69fb4a9f-8210-8326-acf5-90d90975f1e7
            _, broken_arms_trajectory = jnp.where(
                arm_damage.get_active_arms() == 0 # shape (n_envs, n_agents)
            ) # shape (n_envs,)
        else:
            broken_arms_trajectory = jnp.repeat(jnp.inf, self.config.environment.number_of_environments)
            breakpoints_trajectory = jnp.repeat(jnp.inf, self.config.environment.number_of_environments)

        return (
            states_trajectory, 
            actions_trajectory, 
            positions_trajectory, 
            breakpoints_trajectory,
            broken_arms_trajectory
        )

    def _save_actions_trajectory(self, output_filename: str, actions_trajectory: jnp.ndarray) -> None:
        """Saves the action trajectory to a csv file.

        :param output_filename: Filename of the output csv, relative to project root
        :param actions_trajectory: The action trajectory - shape (n_environments, n_steps, n_agents)
        """
        if len(actions_trajectory.shape) == 3:
            n_environments, n_steps, n_agents = actions_trajectory.shape
        else:
            n_steps, n_agents = actions_trajectory.shape
            n_environments = 1
            actions_trajectory = actions_trajectory[:, None, :]

        with open(f'{output_filename}', 'w') as output:
            output.write('environment_id,step_id,agent_id,action\n')

            for environment_id in range(n_environments):
                for step_id in range(n_steps):
                    for agent_id in range(n_agents):
                        output.write(f'{environment_id},{step_id},{agent_id},{actions_trajectory[step_id, environment_id, agent_id]}\n')

    def _save_positions_trajectory(self, output_filename: str, positions_trajectory: jnp.ndarray, target_positions: jnp.ndarray) -> None:
        """Saves the positions trajectory to a csv file.
        
        :param output_filename: Filename of the output csv, relative to project root
        :param positions_trajectory: The positions trajectory - shape (n_environments, n_steps, 3)
        :param target_positions: The positions of the target for each environment - shape (n_environments, 3)
        """
        if len(positions_trajectory.shape) == 3:
            n_environments, n_steps, _ = positions_trajectory.shape
        else:
            n_steps, _ = positions_trajectory.shape
            n_environments = 1
            positions_trajectory = positions_trajectory[None, :, :]

        with open(f'{output_filename}', 'w') as output:
            output.write(f'environment_id,step_id,x,y,in_trajectory\n')

            for environment_id in range(n_environments): 
                output.write(f'{environment_id},0,0,0,true\n') # Start position

                for step_id in range(n_steps):
                    x = positions_trajectory[environment_id, step_id, 0]
                    y = positions_trajectory[environment_id, step_id, 1]
                    output.write(f'{environment_id},{step_id},{x},{y},true\n')

                end_x, end_y, _ = tuple(target_positions[environment_id])
                output.write(f'{environment_id},{n_steps},{end_x},{end_y},false\n') # End position

    def _save_breakpoints_trajectory(self, output_filename: str, breakpoints_trajectory: jnp.ndarray, broken_arms_trajectory: jnp.ndarray) -> None:
        """Saves the breakpoints trajectory to a csv file.
        
        :param output_filename: Filename of the output csv, relative to project root
        :param breakpoints_trajectory: The breakpoints trajectory - shape (n_environments,)
        :param broken_arms_trajectory: The broken arms for each environment - shape (n_environments,)
        """
        n_environments = breakpoints_trajectory.shape[0]

        with open(f'{output_filename}', 'w') as output:
            output.write(f'environment_id,breakpoint,agent_id\n')

            for environment_id in range(n_environments):
                output.write(f'{environment_id},{breakpoints_trajectory[environment_id]},{broken_arms_trajectory[environment_id]}\n')

    def _generate_charts(self, arguments: argparse.Namespace) -> None:
        """Generates the charts written with the Vega-lite API in JavaScript.

        Calls the `plots/index.js` script with appropiate command line arguments.

        :param arguments: Commandline arguments to the `brittle_star_locomotion` module
        """
        project_root = Path(__file__).resolve().parents[3]
        plot_script = str(project_root / "docs" / "plots" / "index.js")

        subprocess.run([
            "node",
            plot_script,
            "eval",
            arguments.output_actions_trajectory,
            arguments.output_breakpoints_trajectory,
            arguments.output_positions_trajectory
        ], check=True)
